public class BoolFlipFlopUtil {
    /**
     * I like when create game in unreal it have flipflop inferface to use
     *
     * @param value The boolean value to be flipped.
     * @return The opposite boolean value.
     * algorith O(1)
     */
    public static boolean flipFlop(boolean value) {
        return !value;
    }
}
